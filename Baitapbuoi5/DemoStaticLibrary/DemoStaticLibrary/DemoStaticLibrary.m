#import "DemoStaticLibrary.h"

@implementation DemoStaticLibrary

-(float)nhan:(float)agmA :(float)agmB

{
    return (agmA*agmB);
}

-(float)chia:(float)agmA :(float)agmB

{
    return (agmA/agmB);
}

-(float)cong:(float)agmA :(float)agmB

{
    return (agmA/agmB);
}

-(float)tru:(float)agmA :(float)agmB

{
    return (agmA-agmB);
}

@end
